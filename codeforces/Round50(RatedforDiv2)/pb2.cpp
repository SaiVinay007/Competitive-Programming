#include <bits/stdc++.h>
using namespace std;

int main(int argc, char const *argv[])
{
    long long q;
    cin >> q;

    for (int i = 0; i<q;i++){

        long long n,m,k;

        cin >>n>>m>>k;

        if ( n>k || -n>k || m>k || -m>k){
            cout<<-1<<endl;
        }




    }


    return 0;
}
